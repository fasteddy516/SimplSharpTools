namespace SimplSharpTools
{
    /// <summary>
    /// Represents ANSI color codes used for terminal text formatting.
    /// </summary>
    public enum ANSIColor
    {
        /// <summary>
        /// No color (default).
        /// </summary>
        None = 0,

        /// <summary>
        /// Black color (ANSI code 30).
        /// </summary>
        Black = 30,

        /// <summary>
        /// Red color (ANSI code 31).
        /// </summary>
        Red = 31,

        /// <summary>
        /// Green color (ANSI code 32).
        /// </summary>
        Green = 32,

        /// <summary>
        /// Yellow color (ANSI code 33).
        /// </summary>
        Yellow = 33,

        /// <summary>
        /// Blue color (ANSI code 34).
        /// </summary>
        Blue = 34,

        /// <summary>
        /// Magenta color (ANSI code 35).
        /// </summary>
        Magenta = 35,

        /// <summary>
        /// Cyan color (ANSI code 36).
        /// </summary>
        Cyan = 36,

        /// <summary>
        /// White color (ANSI code 37).
        /// </summary>
        White = 37,

        /// <summary>
        /// Bright black (gray) color (ANSI code 90).
        /// </summary>
        BrightBlack = 90,

        /// <summary>
        /// Bright red color (ANSI code 91).
        /// </summary>
        BrightRed = 91,

        /// <summary>
        /// Bright green color (ANSI code 92).
        /// </summary>
        BrightGreen = 92,

        /// <summary>
        /// Bright yellow color (ANSI code 93).
        /// </summary>
        BrightYellow = 93,

        /// <summary>
        /// Bright blue color (ANSI code 94).
        /// </summary>
        BrightBlue = 94,

        /// <summary>
        /// Bright magenta color (ANSI code 95).
        /// </summary>
        BrightMagenta = 95,

        /// <summary>
        /// Bright cyan color (ANSI code 96).
        /// </summary>
        BrightCyan = 96,

        /// <summary>
        /// Bright white color (ANSI code 97).
        /// </summary>
        BrightWhite = 97
    }
}
